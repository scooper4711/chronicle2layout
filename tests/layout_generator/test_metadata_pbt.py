"""Property-based tests for layout_generator.metadata module.

Uses hypothesis to verify universal properties of metadata rule
matching across randomly generated inputs.
"""

from __future__ import annotations

import re

from hypothesis import given, assume
from hypothesis import strategies as st

from layout_generator.metadata import (
    MatchedMetadata,
    MetadataConfig,
    MetadataRule,
    apply_substitutions,
    match_rule,
)


# ---------------------------------------------------------------------------
# Strategies
# ---------------------------------------------------------------------------

# Simple literal path segments — these work as regex patterns that match
# themselves, avoiding regex special-character issues.
_path_segment = st.from_regex(r"[a-z][a-z0-9]{0,9}", fullmatch=True)

_path = st.builds(
    lambda parts: "/".join(parts),
    st.lists(_path_segment, min_size=1, max_size=4),
).map(lambda p: p + ".pdf")

_id_template = st.from_regex(r"[a-z][a-z0-9\-]{0,19}", fullmatch=True)
_parent_template = st.from_regex(r"[a-z][a-z0-9\-]{0,19}", fullmatch=True)


def _metadata_rule(pattern: str) -> st.SearchStrategy[MetadataRule]:
    """Build a MetadataRule strategy with a fixed literal pattern."""
    return st.builds(
        MetadataRule,
        pattern=st.just(pattern),
        id=_id_template,
        parent=_parent_template,
    )


# ---------------------------------------------------------------------------
# Property 1: First-match rule semantics
# ---------------------------------------------------------------------------


# Feature: layout-generator, Property 1: First-match rule semantics
@given(
    test_path=_path,
    prefix_rules=st.lists(
        st.tuples(_path_segment, _id_template, _parent_template),
        min_size=0,
        max_size=5,
    ),
    matching_id=_id_template,
    matching_parent=_parent_template,
    suffix_rules=st.lists(
        st.tuples(_path_segment, _id_template, _parent_template),
        min_size=0,
        max_size=5,
    ),
)
def test_first_match_rule_semantics(
    test_path: str,
    prefix_rules: list[tuple[str, str, str]],
    matching_id: str,
    matching_parent: str,
    suffix_rules: list[tuple[str, str, str]],
) -> None:
    """match_rule returns the first matching rule's metadata, ignoring later rules.

    Validates: Requirements 2.7

    Strategy: build an ordered rule list where:
    - prefix rules use patterns that do NOT match the test path
    - one rule uses a pattern guaranteed to match the test path
    - suffix rules may or may not match (doesn't matter)

    The result must always come from the guaranteed-match rule.
    """
    # Build non-matching prefix rules using patterns that won't match
    # the test path. We use "^NOMATCH_<segment>$" which won't match
    # any path generated by _path (those always contain "/" and ".pdf").
    rules: list[MetadataRule] = []
    for segment, rule_id, rule_parent in prefix_rules:
        rules.append(
            MetadataRule(
                pattern=f"^NOMATCH_{segment}$",
                id=rule_id,
                parent=rule_parent,
            )
        )

    # The first matching rule — uses a catch-all pattern
    first_matching_rule = MetadataRule(
        pattern=".*",
        id=matching_id,
        parent=matching_parent,
    )
    rules.append(first_matching_rule)

    # Suffix rules that also match (catch-all) — should be ignored
    for segment, rule_id, rule_parent in suffix_rules:
        rules.append(
            MetadataRule(
                pattern=".*",
                id=rule_id,
                parent=rule_parent,
            )
        )

    config = MetadataConfig(layouts_dir=None, rules=rules)
    result = match_rule(test_path, config)

    assert result is not None
    assert result.id == matching_id
    assert result.parent == matching_parent


