# Literal words used as capture group values — alphanumeric only to avoid
# regex special characters and ensure clean matching.
_capture_word = st.from_regex(r"[a-z]{2,8}", fullmatch=True)


# ---------------------------------------------------------------------------
# Property 11: Regex substitution replaces all valid group references
# ---------------------------------------------------------------------------


# Feature: layout-generator, Property 11: Regex substitution replaces all valid group references
@given(
    words=st.lists(_capture_word, min_size=1, max_size=5),
)
def test_regex_substitution_replaces_valid_references(
    words: list[str],
) -> None:
    """apply_substitutions replaces all valid $k references and leaves invalid ones as literals.

    Validates: Requirements 15.1, 15.2

    Strategy:
    - Generate N literal words as capture group values.
    - Build a regex with N capture groups: (word1)(word2)...(wordN).
    - Build a test string that matches: word1word2...wordN.
    - Build a template containing $0 through $N (all valid) plus $N+1 (invalid).
    - Verify each valid $k is replaced with the correct group value.
    - Verify $N+1 is left as a literal string.
    """
    num_groups = len(words)
    full_match_text = "".join(words)

    # Build regex pattern with one capture group per word
    pattern = "".join(f"({word})" for word in words)
    match = re.match(pattern, full_match_text)
    assert match is not None

    # Build template: "prefix_$0_$1_..._$N_$N+1_suffix"
    invalid_ref = f"${num_groups + 1}"
    parts = [f"${k}" for k in range(num_groups + 1)]
    parts.append(invalid_ref)
    template = "_".join(["prefix"] + parts + ["suffix"])

    result = apply_substitutions(template, match)

    # Build expected: $0 → full match, $1...$N → individual words
    expected_parts = [full_match_text] + words + [invalid_ref]
    expected = "_".join(["prefix"] + expected_parts + ["suffix"])

    assert result == expected
